package br.com.stefanini.operadores;

public class OperadoresAtribuicao {
	public static void main(String[] args) {
		
	int a = 11;
	int b = 5;

	b += a; // aditivo b = b + a	infix
	b -= a; // substrativo	b = b + a
	b *= a; // multiplicativo b = b + a
	b /= a; // divisor b = b + a
	b %= a; // modular b = b + a
	
	System.out.println( a % b);
	
	}
}
